const NUMBERS = [
  'zero',
  'one',
  'two',
  'three',
  'four',
  'five',
  'six',
  'seven',
  'eight',
  'nine',
  'ten',
  'eleven',
  'twelve',
  'thirteen',
];

const pluralize = (word: string, count: number, ending: string = 's') =>
  `${NUMBERS[count] || count} ${word}${count === 1 ? '' : ending}`;

export default pluralize;
